package idat.edu.pe.Models;

public class FacturaModels {
    private String idFactura;
    private String Cargo;
    private String Cliente;
    private String Facturista;
    private String TipoPago;
    private String TipoMoneda;
    private String Empresa;
    private String FechaExp;
    private String hrExpe;
    private String EstadoFac;
    
    //Get And Set
	public String getIdFactura() {
		return idFactura;
	}
	public void setIdFactura(String idFactura) {
		this.idFactura = idFactura;
	}
	public String getCargo() {
		return Cargo;
	}
	public void setCargo(String cargo) {
		Cargo = cargo;
	}
	public String getCliente() {
		return Cliente;
	}
	public void setCliente(String cliente) {
		Cliente = cliente;
	}
	public String getFacturista() {
		return Facturista;
	}
	public void setFacturista(String facturista) {
		Facturista = facturista;
	}
	public String getTipoPago() {
		return TipoPago;
	}
	public void setTipoPago(String tipoPago) {
		TipoPago = tipoPago;
	}
	public String getTipoMoneda() {
		return TipoMoneda;
	}
	public void setTipoMoneda(String tipoMoneda) {
		TipoMoneda = tipoMoneda;
	}
	public String getEmpresa() {
		return Empresa;
	}
	public void setEmpresa(String empresa) {
		Empresa = empresa;
	}
	public String getFechaExp() {
		return FechaExp;
	}
	public void setFechaExp(String fechaExp) {
		FechaExp = fechaExp;
	}
	public String getHrExpe() {
		return hrExpe;
	}
	public void setHrExpe(String hrExpe) {
		this.hrExpe = hrExpe;
	}
	public String getEstadoFac() {
		return EstadoFac;
	}
	public void setEstadoFac(String estadoFac) {
		EstadoFac = estadoFac;
	}

   
	

    
}
