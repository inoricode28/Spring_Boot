package idat.edu.pe.Models;

public class PersonalMedicoModels {
    private String idPerMedico;
    private String Nombre;
    private String apPaterno;
    private String apMaterno;
    private String Telefono;
    private String Direccion;
    private String Horario;
    private String Especialidad;
    private String Salario;
    private String MedAsignado;

    //Get And Set
	public String getIdPerMedico() {
		return idPerMedico;
	}
	public void setIdPerMedico(String idPerMedico) {
		this.idPerMedico = idPerMedico;
	}
	public String getNombre() {
		return Nombre;
	}
	public void setNombre(String nombre) {
		Nombre = nombre;
	}
	public String getApPaterno() {
		return apPaterno;
	}
	public void setApPaterno(String apPaterno) {
		this.apPaterno = apPaterno;
	}
	public String getApMaterno() {
		return apMaterno;
	}
	public void setApMaterno(String apMaterno) {
		this.apMaterno = apMaterno;
	}
	public String getTelefono() {
		return Telefono;
	}
	public void setTelefono(String telefono) {
		Telefono = telefono;
	}
	public String getDireccion() {
		return Direccion;
	}
	public void setDireccion(String direccion) {
		Direccion = direccion;
	}
	public String getHorario() {
		return Horario;
	}
	public void setHorario(String horario) {
		Horario = horario;
	}
	public String getEspecialidad() {
		return Especialidad;
	}
	public void setEspecialidad(String especialidad) {
		Especialidad = especialidad;
	}
	public String getSalario() {
		return Salario;
	}
	public void setSalario(String salario) {
		Salario = salario;
	}
	public String getMedAsignado() {
		return MedAsignado;
	}
	public void setMedAsignado(String medAsignado) {
		MedAsignado = medAsignado;
	}
    
    
    

}
