package idat.edu.pe.Models;

public class PacienteModels {

    private String idPaciente;
    private String Nombre;
    private String apPaterno;
    private String apMaterno;
    private String tipSangre;
    private String fechaSalida;
    private String fechaEntrada;
    private String Genero;
    private String idRegistro;
    private String idHabitacion;
    private String atiPaciente;

    //Get And Set
	public String getIdPaciente() {
		return idPaciente;
	}
	public void setIdPaciente(String idPaciente) {
		this.idPaciente = idPaciente;
	}
	public String getNombre() {
		return Nombre;
	}
	public void setNombre(String nombre) {
		Nombre = nombre;
	}
	public String getApPaterno() {
		return apPaterno;
	}
	public void setApPaterno(String apPaterno) {
		this.apPaterno = apPaterno;
	}
	public String getApMaterno() {
		return apMaterno;
	}
	public void setApMaterno(String apMaterno) {
		this.apMaterno = apMaterno;
	}
	public String getTipSangre() {
		return tipSangre;
	}
	public void setTipSangre(String tipSangre) {
		this.tipSangre = tipSangre;
	}
	public String getFechaSalida() {
		return fechaSalida;
	}
	public void setFechaSalida(String fechaSalida) {
		this.fechaSalida = fechaSalida;
	}
	public String getFechaEntrada() {
		return fechaEntrada;
	}
	public void setFechaEntrada(String fechaEntrada) {
		this.fechaEntrada = fechaEntrada;
	}
	public String getGenero() {
		return Genero;
	}
	public void setGenero(String genero) {
		Genero = genero;
	}
	public String getIdRegistro() {
		return idRegistro;
	}
	public void setIdRegistro(String idRegistro) {
		this.idRegistro = idRegistro;
	}
	public String getIdHabitacion() {
		return idHabitacion;
	}
	public void setIdHabitacion(String idHabitacion) {
		this.idHabitacion = idHabitacion;
	}
	public String getAtiPaciente() {
		return atiPaciente;
	}
	public void setAtiPaciente(String atiPaciente) {
		this.atiPaciente = atiPaciente;
	}  

}
